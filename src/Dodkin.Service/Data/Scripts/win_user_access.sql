-- grant access from Dodkin db to Local Service account

USE [master]
GO
CREATE LOGIN [NT AUTHORITY\LOCAL SERVICE] FROM WINDOWS WITH DEFAULT_DATABASE=[master]
GO
USE [Dodkin]
GO
CREATE USER [NT AUTHORITY\LOCAL SERVICE] FOR LOGIN [NT AUTHORITY\LOCAL SERVICE]
GO
USE [Dodkin]
GO
ALTER USER [NT AUTHORITY\LOCAL SERVICE] WITH DEFAULT_SCHEMA=[job]
GO
USE [Dodkin]
GO
ALTER ROLE [db_datareader] ADD MEMBER [NT AUTHORITY\LOCAL SERVICE]
GO
USE [Dodkin]
GO
ALTER ROLE [db_datawriter] ADD MEMBER [NT AUTHORITY\LOCAL SERVICE]
GO
use [Dodkin]
GO
GRANT DELETE ON SCHEMA::[job] TO [NT AUTHORITY\LOCAL SERVICE]
GO
use [Dodkin]
GO
GRANT EXECUTE ON SCHEMA::[job] TO [NT AUTHORITY\LOCAL SERVICE]
GO
use [Dodkin]
GO
GRANT INSERT ON SCHEMA::[job] TO [NT AUTHORITY\LOCAL SERVICE]
GO
use [Dodkin]
GO
GRANT SELECT ON SCHEMA::[job] TO [NT AUTHORITY\LOCAL SERVICE]
GO
use [Dodkin]
GO
GRANT UPDATE ON SCHEMA::[job] TO [NT AUTHORITY\LOCAL SERVICE]
GO
use [Dodkin]
GO
GRANT DELETE ON SCHEMA::[dbo] TO [NT AUTHORITY\LOCAL SERVICE]
GO
use [Dodkin]
GO
GRANT EXECUTE ON SCHEMA::[dbo] TO [NT AUTHORITY\LOCAL SERVICE]
GO
use [Dodkin]
GO
GRANT INSERT ON SCHEMA::[dbo] TO [NT AUTHORITY\LOCAL SERVICE]
GO
use [Dodkin]
GO
GRANT SELECT ON SCHEMA::[dbo] TO [NT AUTHORITY\LOCAL SERVICE]
GO
use [Dodkin]
GO
GRANT UPDATE ON SCHEMA::[dbo] TO [NT AUTHORITY\LOCAL SERVICE]
GO
